def world() -> str:
    return "world"


def add(a: int, b: int) -> int:
    """
    Adds two integers together.

    Args:
        a: The first integer to add.
        b: The second integer to add.

    Returns:
        The sum of the two integers.
    """
    return a + b

def minus(a: int, b: int) -> int:
    return a - b